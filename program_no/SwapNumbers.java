package program_no;

public class SwapNumbers {

	public static void main(String[] args) {
		
		int x=10;
		int y = 20;
		int z =0;
		System.out.println("before swap numbers are:- " +x  +" , " +y);
		z = x;
		x = y;
		y = z;
		System.out.println("after swap numbers are:- " +x  +" , " +y);
		

	}

}
